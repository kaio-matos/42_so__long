#include <libft.h>

char	*ft_strrchr(const char *s, int c)
{
	int	i;

	i = ft_strlen(s);
	if (c > 127)
		c %= 256;
	while (i && s[i] != c)
		i--;
	s += i;
	if (*s == c)
		return ((char *)s);
	return (NULL);
}
