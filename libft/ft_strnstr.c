#include <libft.h>

char	*ft_strnstr(const char *big, const char *little, size_t len)
{
	size_t	i;
	size_t	j;
	size_t	length;

	i = 0;
	length = ft_strlen(little);
	if (length == 0)
		return ((char *)big);
	while (big[i] != '\0' && i < len)
	{
		j = 0;
		while (big[i + j] == little[j] && little[j] != '\0' && (j + i) < len)
			j++;
		if (j == length)
			return ((char *)&big[i]);
		i++;
	}
	return (NULL);
}
